def test_get_transaction_hash_by_pointer(conf):
    if not conf["option_transaction"]:
        return

    r = requests.get(conf["base_url"] +
                     "/rest/transaction/hash/by/pointer/37656:1")
    assert r.status_code == 200
    d = r.json()["data"]
    assert d == "9c8126c364dee92c13831b22f939655ee543c154fe6c0dfcb1e087bc293e4b24"
    r = requests.get(conf["base_url"] +
                     "/rest/transaction/hash/by/pointer/32656:1")
    assert r.status_code == 200
    d = r.json()["data"]
    assert d == "88e1822895380789a9d822abc913e25135a77d5792ce23704e592cc5e2450564"


def test_get_transaction_hash_by_pointers(conf):
    if not conf["option_transaction"]:
        return
    tlp = ["37656:1", "32656:1"]
    r = requests.post(conf["base_url"] + "/rest/transactions/hash/by/blockchain/pointer/list",
                      data= json.dumps(tlp).encode())
    assert r.status_code == 200
    dl2 = r.json()["data"]

    tl = ["9c8126c364dee92c13831b22f939655ee543c154fe6c0dfcb1e087bc293e4b24",
          "88e1822895380789a9d822abc913e25135a77d5792ce23704e592cc5e2450564"]

    for t in dl2.values():
        assert t in tl
